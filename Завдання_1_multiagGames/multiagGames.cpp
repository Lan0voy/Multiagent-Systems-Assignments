using namespace std;

// Выигрыш хранит результат двух игроков.
// first  - выигрыш первого игрока.
// second - выигрыш второго игрока.
using Payoff = pair<int, int>;

// Структура Game хранит данные одной игры.
struct Game {
    string name;                         // Название игры
    string situation;                    // Краткое описание ситуации игры
    vector<string> rowActions;           // Стратегии первого игрока, строки матрицы
    vector<string> colActions;           // Стратегии второго игрока, столбцы матрицы
    vector<vector<Payoff>> matrix;       // Матрица выигрышей
};

// Функция выводит матрицу выигрышей.
void printMatrix(const Game& game) {
    cout << "\n====================================\n";
    cout << game.name << endl;
    cout << "====================================\n\n";

    // Выводим описание ситуации игры.
    cout << "Ситуация игры:\n";
    cout << game.situation << "\n\n";

    // Поясняем формат записи выигрышей.
    cout << "Формат выигрыша: (Игрок 1, Игрок 2)\n\n";

    // Выводим стратегии второго игрока.
    cout << "                    ";
    for (const string& colName : game.colActions) {
        cout << colName << "          ";
    }
    cout << endl;

    // Выводим строки матрицы.
    for (int i = 0; i < game.rowActions.size(); i++) {
        // Выводим стратегию первого игрока.
        cout << game.rowActions[i] << "          ";

        // Выводим выигрыши для каждой пары стратегий.
        for (int j = 0; j < game.colActions.size(); j++) {
            cout << "("
                << game.matrix[i][j].first
                << ", "
                << game.matrix[i][j].second
                << ")          ";
        }

        cout << endl;
    }

    cout << endl;
}

// Функция ищет строго доминируемые стратегии.
// Строго доминируемая стратегия всегда хуже другой стратегии
// при любых действиях второго игрока.
void findDominatedStrategies(const Game& game) {
    cout << "Анализ IESDA:\n";

    // Переменная показывает, найдена ли хотя бы одна доминируемая стратегия.
    bool foundDominated = false;

    // Количество стратегий первого игрока.
    int rows = game.rowActions.size();

    // Количество стратегий второго игрока.
    int cols = game.colActions.size();

    // Проверяем стратегии первого игрока.
    for (int x = 0; x < rows; x++) {
        // x - стратегия, которую проверяем.
        // y - другая стратегия, с которой сравниваем.
        for (int y = 0; y < rows; y++) {
            // Нельзя сравнивать стратегию саму с собой.
            if (x == y) {
                continue;
            }

            // Сначала считаем, что стратегия x доминируется стратегией y.
            bool dominated = true;

            // Проверяем выигрыш первого игрока при всех стратегиях второго игрока.
            for (int j = 0; j < cols; j++) {
                // Если стратегия y не даёт строго больший выигрыш,
                // значит стратегия x не является строго доминируемой.
                if (game.matrix[y][j].first <= game.matrix[x][j].first) {
                    dominated = false;
                }
            }

            // Если стратегия x строго доминируется стратегией y,
            // выводим результат.
            if (dominated) {
                foundDominated = true;

                cout << "Игрок 1: стратегия \""
                    << game.rowActions[x]
                    << "\" строго доминируется стратегией \""
                    << game.rowActions[y]
                    << "\".\n";
            }
        }
    }

    // Проверяем стратегии второго игрока.
    for (int x = 0; x < cols; x++) {
        // x - стратегия второго игрока, которую проверяем.
        // y - другая стратегия второго игрока.
        for (int y = 0; y < cols; y++) {
            // Нельзя сравнивать стратегию саму с собой.
            if (x == y) {
                continue;
            }

            // Сначала считаем, что стратегия x доминируется стратегией y.
            bool dominated = true;

            // Проверяем выигрыш второго игрока при всех стратегиях первого игрока.
            for (int i = 0; i < rows; i++) {
                // Для второго игрока используем second.
                if (game.matrix[i][y].second <= game.matrix[i][x].second) {
                    dominated = false;
                }
            }

            // Если стратегия x строго доминируется стратегией y,
            // выводим результат.
            if (dominated) {
                foundDominated = true;

                cout << "Игрок 2: стратегия \""
                    << game.colActions[x]
                    << "\" строго доминируется стратегией \""
                    << game.colActions[y]
                    << "\".\n";
            }
        }
    }

    // Если строго доминируемых стратегий нет, выводим сообщение.
    if (!foundDominated) {
        cout << "Строго доминируемых стратегий не найдено.\n";
    }

    cout << endl;
}

// Функция ищет равновесия Нэша в чистых стратегиях.
// Равновесие Нэша означает, что ни один игрок не может улучшить свой выигрыш,
// если изменит только свою стратегию.
void findPureNashEquilibria(const Game& game) {
    cout << "Чистые равновесия Нэша:\n";

    // Переменная показывает, найдено ли хотя бы одно равновесие.
    bool found = false;

    // Количество стратегий первого игрока.
    int rows = game.rowActions.size();

    // Количество стратегий второго игрока.
    int cols = game.colActions.size();

    // Перебираем все клетки матрицы.
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            // Проверяем, является ли текущая стратегия первого игрока
            // лучшим ответом на стратегию второго игрока.
            bool bestForPlayer1 = true;

            // Проверяем, является ли текущая стратегия второго игрока
            // лучшим ответом на стратегию первого игрока.
            bool bestForPlayer2 = true;

            // Первый игрок может менять только строку.
            // Стратегия второго игрока остаётся неизменной.
            for (int otherRow = 0; otherRow < rows; otherRow++) {
                // Если первый игрок может получить больше,
                // выбрав другую строку, значит это не равновесие Нэша.
                if (game.matrix[otherRow][j].first > game.matrix[i][j].first) {
                    bestForPlayer1 = false;
                }
            }

            // Второй игрок может менять только столбец.
            // Стратегия первого игрока остаётся неизменной.
            for (int otherCol = 0; otherCol < cols; otherCol++) {
                // Если второй игрок может получить больше,
                // выбрав другой столбец, значит это не равновесие Нэша.
                if (game.matrix[i][otherCol].second > game.matrix[i][j].second) {
                    bestForPlayer2 = false;
                }
            }

            // Если оба игрока выбрали лучшие ответы друг на друга,
            // текущая клетка является равновесием Нэша.
            if (bestForPlayer1 && bestForPlayer2) {
                found = true;

                cout << "("
                    << game.rowActions[i]
                    << ", "
                    << game.colActions[j]
                    << ") ";

                cout << "выигрыш = ("
                    << game.matrix[i][j].first
                    << ", "
                    << game.matrix[i][j].second
                    << ")\n";
            }
        }
    }

    // Если чистых равновесий Нэша нет, выводим сообщение.
    if (!found) {
        cout << "Чистое равновесие Нэша не найдено.\n";

        // Для игры в монетку известно смешанное равновесие.
        if (game.name == "Игра в монетку") {
            cout << "Смешанное равновесие: каждый игрок выбирает \"Орёл\" и \"Решка\" с вероятностью 0.5.\n";
        }
    }

    cout << endl;
}

// Функция полностью анализирует одну игру.
void analyzeGame(const Game& game) {
    // Выводим матрицу игры.
    printMatrix(game);

    // Ищем строго доминируемые стратегии.
    findDominatedStrategies(game);

    // Ищем чистые равновесия Нэша.
    findPureNashEquilibria(game);
}

int main() {
    // Настройка локали для русского текста.
    setlocale(LC_ALL, "");

#ifdef _WIN32
    // Настройка кодировки UTF-8 для Windows-консоли.
    SetConsoleOutputCP(CP_UTF8);
    SetConsoleCP(CP_UTF8);
#endif

    // Создаём первую игру: дилемма заключённого.
    Game prisonersDilemma;

    prisonersDilemma.name = "Дилемма заключённого";

    prisonersDilemma.situation =
        "Два заключённых выбирают: молчать или признаться. "
        "Каждый принимает решение независимо. "
        "Если один признаётся, а другой молчит, признавшийся получает лучший результат.";

    prisonersDilemma.rowActions = { "Молчать", "Признаться" };
    prisonersDilemma.colActions = { "Молчать", "Признаться" };

    // Матрица выигрышей:
    // оба молчат: (3, 3)
    // первый молчит, второй признаётся: (0, 4)
    // первый признаётся, второй молчит: (4, 0)
    // оба признаются: (1, 1)
    prisonersDilemma.matrix = {
        {{3, 3}, {0, 4}},
        {{4, 0}, {1, 1}}
    };

    // Создаём вторую игру: игра в монетку.
    Game matchingPennies;

    matchingPennies.name = "Игра в монетку";

    matchingPennies.situation =
        "Два игрока одновременно выбирают сторону монеты: орёл или решка. "
        "Первый игрок выигрывает, если выборы совпали. "
        "Второй игрок выигрывает, если выборы различаются.";

    matchingPennies.rowActions = { "Орёл", "Решка" };
    matchingPennies.colActions = { "Орёл", "Решка" };

    // Матрица выигрышей:
    // если выборы совпали, первый игрок получает 1, второй получает -1.
    // если выборы различаются, первый игрок получает -1, второй получает 1.
    matchingPennies.matrix = {
        {{1, -1}, {-1, 1}},
        {{-1, 1}, {1, -1}}
    };

    // Создаём третью игру: игра на перекрёстке.
    Game crossroadGame;

    crossroadGame.name = "Игра на перекрёстке";

    crossroadGame.situation =
        "Два водителя подъезжают к перекрёстку. "
        "Каждый может ехать или остановиться. "
        "Если оба едут, возникает конфликт. "
        "Если один едет, а другой стоит, выигрывает тот, кто проехал.";

    crossroadGame.rowActions = { "Ехать", "Остановиться" };
    crossroadGame.colActions = { "Ехать", "Остановиться" };

    // Матрица выигрышей:
    // оба едут: (-1, -1)
    // первый едет, второй стоит: (1, 0)
    // первый стоит, второй едет: (0, 1)
    // оба стоят: (0, 0)
    crossroadGame.matrix = {
        {{-1, -1}, {1, 0}},
        {{0, 1}, {0, 0}}
    };

    // Анализируем все три игры.
    analyzeGame(prisonersDilemma);
    analyzeGame(matchingPennies);
    analyzeGame(crossroadGame);

    // Завершаем программу.
    return 0;
}